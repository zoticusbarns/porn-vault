<template>
  <WidgetCard title="Stats" icon="mdi-counter">
    <div class="my-3">
      <span class="mr-2 d-inline-block display-1">{{ numScenes }}</span>
      <span class="subtitle-1">scenes</span>
    </div>
    <v-divider></v-divider>

    <div class="my-3">
      <span class="mr-2 d-inline-block display-1">{{ numActors }}</span>
      <span class="subtitle-1">{{ actorPlural.toLowerCase() }}</span>
    </div>
    <v-divider></v-divider>

    <div class="my-3">
      <span class="mr-2 d-inline-block display-1">{{ numMovies }}</span>
      <span class="subtitle-1">movies</span>
    </div>
    <v-divider></v-divider>

    <div class="my-3">
      <span class="mr-2 d-inline-block display-1">{{ numImages }}</span>
      <span class="subtitle-1">images</span>
    </div>
  </WidgetCard>
</template>

<script lang="ts">
import { Component, Vue } from "vue-property-decorator";
import ApolloClient from "@/apollo";
import gql from "graphql-tag";
import { contextModule } from "@/store/context";

@Component({
  components: {},
})
export default class Stats extends Vue {
  infoInterval = null as NodeJS.Timeout | null;

  numScenes = 0;
  numActors = 0;
  numMovies = 0;
  numImages = 0;

  get actorSingular() {
    return contextModule.actorSingular;
  }

  get actorPlural() {
    return contextModule.actorPlural;
  }

  created() {
    this.getInfo();
    this.infoInterval = setInterval(() => {
      this.getInfo();
    }, 30000);
  }

  destroyed() {
    if (this.infoInterval) {
      clearInterval(this.infoInterval);
    }
  }

  async getInfo() {
    ApolloClient.query({
      query: gql`
        {
          numScenes
          numActors
          numMovies
          numImages
        }
      `,
    })
      .then((res) => {
        this.numScenes = res.data.numScenes;
        this.numActors = res.data.numActors;
        this.numMovies = res.data.numMovies;
        this.numImages = res.data.numImages;
      })
      .catch((err) => {
        console.error(err);
      });
  }
}
</script>
